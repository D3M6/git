package evaluacion3;

public class MyClass16 {
	
	/**
	 * Programacion orientada a objetos
	 * 
	 * Es un estilo de programacion que pretende hacer el pensar al programar
	 * mas cercano al pensar acerca del mundo real.
	 * 
	 * Cada objeto es una unidad independiente con una ID unica.
	 * Emjemplo: una manzana es unica así como lo es una taza.
	 * 
	 * Los objetos tambien tienen caracteristicas, son llamadas atributos.
	 * En resumen, en la programacion orientada a objetos,
	 * cada objeto tiene tres dimensiones: identidad, atributos y comportamiento.
	 * 
	 * Clases
	 * 
	 * Una clase describe lo que el objeto será, pero está separada del objeto mismo.
	 * Cada clase tiene un nombre y cada una es utilizada para definir atributos y comportamiento.
	 * 
	 * Atributo: nombre, alto, ancho, genero, edad.
	 * Comportamiento: caminar, correr, saltar, hablar, dormir.
	 * 
	 * En otras palabras, un objeto es una instancia de uina clase.
	 * 
	 * Metodos
	 * 
	 * Definen un comportamiento. Un metodo es una coleccioon de declaraciones
	 * que estan agrupadas juntas para ejecutar una operacion.
	 */
	
	// Crear metodo para sacar en consola: Hola
	static void hola () {
		System.out.println("Hola");
	}

	public static void main(String[] args) {
		// Utilizar el metodo hola() en la main
		hola();
	}

}
