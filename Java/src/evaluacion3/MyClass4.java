package evaluacion3;

public class MyClass4 {

	public static void main(String[] args) {
		
		/**
		 * Operadores de asignacion
		 */
		
		int value = 5;
		
		/**
		 * Adicion y asignacion
		 */
		int num1 = 4;
		int num2 = 8;
		
		num1 += num2; // num1 = num1 + num2;
		
		// Ahora num1 es 12 y num2 es 8
		
		/**
		 * Sustracion y asignacion
		 */
		
		int n1 = 4;
		int n2 = 8;
		n2 -= n1; // n2 = n2 - n1;
		
		// Ahora n2 es 4 y n1 es 4

	}

}
